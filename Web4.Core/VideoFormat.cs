namespace Web4.Core
{
    /// <summary>
    /// Video Format.
    /// </summary>
    public enum VideoFormat
    {
        /// <summary>
        /// None.
        /// </summary>
        None,

        /// <summary>
        /// Real Media (v5 and up).
        /// </summary>
        rm,

        /// <summary>
        ///  ASF (Advanced / Active Streaming Format).
        /// </summary>
        asf,

        /// <summary>
        /// Quicktime MOV.
        /// </summary>
        mov,

        /// <summary>
        /// AVI.
        /// </summary>
        avi,
    }
}
